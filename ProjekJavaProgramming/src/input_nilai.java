    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String nama =jTable1.getValueAt(baris, 2).toString();
        txt_nama.setText(nama);
        String nim = jTable1.getValueAt(baris,1).toString();
        txt_nim.setText(nim);
        
        String english_value = jTable1.getValueAt(baris,3).toString();
        english.setText(english_value);
        String mathematic_value = jTable1.getValueAt(baris,4).toString();
        mathematic.setText(mathematic_value);
        String religion_value = jTable1.getValueAt(baris,5).toString();
        religion.setText(religion_value);
        String attitude_value = jTable1.getValueAt(baris,6).toString();
        attitude.setText(attitude_value);
        
    }//GEN-LAST:event_jTable1MouseClicked

