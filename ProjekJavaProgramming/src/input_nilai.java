    private void load_pencarian(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("English");
        model.addColumn("Mathematic");
        model.addColumn("Religion");
        model.addColumn("Attitude");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from nilai where nama like  '"+"%"+pencarian.getText()+"%"+"' ";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
        }
    }
    
